"""
A simple calculator module for testing ARC indexing
"""

class Calculator:
    """
    A simple calculator class with basic operations
    """
    
    def add(self, a, b):
        """
        Adds two numbers
        
        Args:
            a: First number
            b: Second number
            
        Returns:
            Sum of a and b
        """
        return a + b
    
    def subtract(self, a, b):
        """
        Subtracts second number from first
        
        Args:
            a: First number
            b: Second number to subtract
            
        Returns:
            Difference of a and b
        """
        return a - b


def multiply(a, b):
    """
    A utility function to multiply two numbers
    
    Args:
        a: First number
        b: Second number
        
    Returns:
        Product of a and b
    """
    return a * b
